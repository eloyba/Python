#Solictamos 2 numero que se guardaran como enteros
numero1=int(input("Introduce un numero: "))
numero2=int(input("Introduce un segundo numero: "))

#Vamos a indicar que operacion se realiza y mostrar el resultado
print ("Se suman los numeros:", numero1 + numero2)
print ("Se restan los numeros:", numero1 - numero2) 
print ("Se multiplican los numeros:", numero1 * numero2)
print ("Se dividen los numeros:", numero1 / numero2)
print ("Se dividen los numeros y solo se muestra el resto:", numero1 % numero2)